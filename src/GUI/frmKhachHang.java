    public boolean kiemTraMaKH() {
        boolean check = true;
        for (int i = 0; i < listKH.size(); i++) {
            if (listKH.get(i).getMaKhachHang().equals(txtMaKH.getText())) {
                check = false;
            }
        }
        return check;
    }

    public boolean kiemTraRong() {
        if (txtMaKH.getText().isEmpty()
                || txtHoVaTen.getText().isEmpty()
                || txtEmail.getText().isEmpty() || txtSoDienThoai.getText().isEmpty() || txtDiaChi.getText().isEmpty()) {
         return false;        
        }
        return true;
    }
