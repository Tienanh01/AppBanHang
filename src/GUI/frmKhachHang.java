    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(kiemTraRong() == false){
            JOptionPane.showMessageDialog(this,"Bạn chưa nhập đủ thông tin");
        }
        else if (kiemTraMaKH() == false) {
            JOptionPane.showMessageDialog(this, "Mã Khách hàng đã tồn tại ");
        } else if (validate.kiemTraEmail(txtEmail.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Bạn nhập sai định dạng email");
        } else if (kiemTraSoDienThoai(txtSoDienThoai.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Bạn nhập không đủ số điện thoại");
        } 
        else {
            KhachHang kh = new KhachHang();
            kh.setMaKhachHang(txtMaKH.getText());
            kh.setHoTenKhachHang(txtHoVaTen.getText());
            kh.setGioiTinh(String.valueOf(txtGioiTinh.getSelectedItem()));
            kh.setEmail(txtEmail.getText());
            kh.setSoDienThoai(txtSoDienThoai.getText());
            kh.setDiaChi(txtDiaChi.getText());
            kh.setTrangThai((String) txtTrangThai.getSelectedItem());
            listKH.add(kh);
            ghiFile();
            dayDuLieuLenTable(listKH);
            resetDuLieu();
        }
//       
    }//GEN-LAST:event_jButton1ActionPerformed

