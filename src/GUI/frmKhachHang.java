 public void loadFile1() {
        FileReader fr;
        try {
            fr = new FileReader("khachhang.txt");
            BufferedReader br = new BufferedReader(fr);
            String s = br.readLine();
            listKH = new ArrayList<>();
            while (s != null) {
                String arr[] = s.split("\t");
                KhachHang kh = new KhachHang();
                kh.setMaKhachHang(arr[0]);
                kh.setHoTenKhachHang(arr[1]);
                kh.setGioiTinh(arr[2]);
                kh.setEmail(arr[3]);
                kh.setSoDienThoai(arr[4]);
                kh.setDiaChi(arr[5]);
                kh.setTrangThai(arr[6]);
                listKH.add(kh);
                s = br.readLine();
            }
            fr.close();
            br.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(frmKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(frmKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    // ghi file 
    public void ghiFile() {
        try {
            FileWriter fw = new FileWriter("khachhang.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            String duLieuCanGhi = "";

            for (int i = 0; i < listKH.size(); i++) {
                KhachHang khachHang = listKH.get(i);
                String row = "";
                row += khachHang.getMaKhachHang() + "\t";
                row += khachHang.getHoTenKhachHang() + "\t";
                row += khachHang.getGioiTinh() + "\t";
                row += khachHang.getEmail() + "\t";
                row += khachHang.getSoDienThoai() + "\t";
                row += khachHang.getDiaChi() + "\t";
                row += khachHang.getTrangThai() + "\n";
                duLieuCanGhi += row;
            }

            bw.write(duLieuCanGhi);
            bw.close();

            fw.close();

        } catch (IOException ex) {
            Logger.getLogger(frmKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void dayDuLieuLenTable(ArrayList<KhachHang> list) {      
        defaultTableModel = new DefaultTableModel();
        // sét tiêu đề cho từng dòng 
        defaultTableModel.addColumn("Mã KH");
        defaultTableModel.addColumn("Họ tên KH");
        defaultTableModel.addColumn("Giới tính");
        defaultTableModel.addColumn("Email");
        defaultTableModel.addColumn("SĐT");
        defaultTableModel.addColumn("Địa chỉ");
        defaultTableModel.addColumn("Trạng thái");
//           Lấy giá trị từng cột đọc từ file sau đó ghi thành dòng với mỗi dòng chứa 1 giá trị của cột
        for (KhachHang khachHang : list) { // chú ý chỗ này là list của arraylist truyền vào
            Vector vt = new Vector();
            vt.add(khachHang.getMaKhachHang());
            vt.add(khachHang.getHoTenKhachHang());
            vt.add(khachHang.getGioiTinh());
            vt.add(khachHang.getEmail());
            vt.add(khachHang.getSoDienThoai());
            vt.add(khachHang.getDiaChi());
            vt.add(khachHang.getTrangThai());
            defaultTableModel.addRow(vt);
          
        }
  tbl_nhanvien.setModel(defaultTableModel);
    }

